  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn('flex items-center mt-4 pt-4 border-t border-gray-200', className)}
    {...props}
  />
));

CardFooter.displayName = 'CardFooter';

